// VARIABLES

// let - const

let edad = 25;

let nombre = "Luis";

const apellido = "Cerna";
console.log(edad);
console.log(nombre);
console.log(apellido);

// TIPOS DE DATOS

// Strings --> cadenas de texto

let frase = "hola como estas?";
let frase2 = "hola como estás?";

// concatenación

let nombreUsuario = "Pepito";

console.log("hola " + nombreUsuario);

// TEMPLATE LITERAL ----> backticks (interpolación)

console.log(`Hola ${nombreUsuario} como estás?`);

// Numbers --> números

let altura = 176.6;
let telefono = "1234567890";

console.log(typeof altura);
console.log(typeof telefono);

// Booleans --> booleanos --> true or false

let estaLogeado = true;

console.log(typeof estaLogeado);

// OPERADORES

// Matemáticos

let a = 2;
let b = 5;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);

// Comparación

let nom = "juancito";
let esMayor = true;

console.log(esMayor == true);
console.log(nom == "pepe");

let n1 = 1;
let n2 = 2;

console.log(n1 == n2); // comparación simple
console.log(n1 != n2); // comparación simple
console.log(n1 === n2); // comparación estricta
console.log(n1 !== n2); // comparación estricta
console.log(n1 > n2);
console.log(n1 < n2);
console.log(n1 >= n2);
